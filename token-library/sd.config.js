/**
 * CSS: Single file with mode layers (standard global approach)
 * - Default values (lightMode, sans, desktop baseline)
 * - [data-theme="dark"] overrides
 * - [data-typeface="..."] overrides
 * - @media for responsive font sizes
 */
StyleDictionary.registerFormat({
  name: 'css/standard-modes',
  formatter: ({ dictionary }) => {
    const allTokens = dictionary.allTokens;

    // Collect tokens by category
    const colorTokens = allTokens.filter((t) => t.$type === 'color');
    const fontFamilyTokens = allTokens.filter(
      (t) => t.$type === 'string' && t.scopes?.includes('FONT_FAMILY')
    );
    const fontSizeTokens = allTokens.filter(
      (t) =>
        t.$type === 'dimension' &&
        (t.scopes?.includes('FONT_SIZE') || t.path.some((p) => p.toLowerCase().includes('size')))
    );
    const typographyComposites = allTokens.filter((t) => t.$type === 'typography');

    // Extract modes
    const colourModes = extractModes(colorTokens, 'Colour Theme');
    const typefaceModes = extractModes(
      allTokens.filter((t) => t.path.join('.') === 'fontFamilyMode'),
      'Typeface'
    );

    let css = `/* ─────────────────────────────────────────── */
`;
    css += `/* Design Tokens — Auto-generated by Style Dictionary */
`;
    css += `/* Source: source-tokens/tokens.json */
`;
    css += `/* ─────────────────────────────────────────── */

`;

    // ── BASE LAYER (lightMode defaults) ──
    css += `:root {
`;

    // Colors (lightMode defaults)
    colorTokens.forEach((t) => {
      const val = getResolvedValue(t, dictionary);
      css += `  --${t.path.join('-')}: ${val};
`;
    });

    // Font families (sans default)
    const fontFamilyMode = allTokens.find((t) => t.path.join('.') === 'fontFamilyMode');
    if (fontFamilyMode) {
      const defaultFamily = getResolvedValue(fontFamilyMode, dictionary);
      css += `  --font-family-base: ${defaultFamily};
`;
    }

    // Primitive font sizes
    fontSizeTokens.forEach((t) => {
      const val = getResolvedValue(t, dictionary);
      css += `  --${t.path.join('-')}: ${val};
`;
    });

    // Typography composites
    typographyComposites.forEach((t) => {
      const v = t.$value;
      const basePath = t.path.join('-');
      Object.entries(v).forEach(([prop, propVal]) => {
        if (typeof propVal === 'string' && propVal.startsWith('{')) {
          propVal = resolveReference(propVal, dictionary);
        }
        css += `  --${basePath}-${prop}: ${propVal};
`;
      });
    });

    // Spacing / dimensions
    allTokens
      .filter((t) => t.$type === 'dimension' && t.scopes?.includes('GAP'))
      .forEach((t) => {
        const val = getResolvedValue(t, dictionary);
        css += `  --${t.path.join('-')}: ${val};
`;
      });

    // Radius
    allTokens
      .filter((t) => t.$type === 'dimension' && t.scopes?.includes('CORNER_RADIUS'))
      .forEach((t) => {
        const val = getResolvedValue(t, dictionary);
        css += `  --${t.path.join('-')}: ${val};
`;
      });

    css += `}

`;

    // ── DARK MODE LAYER ──
    if (colourModes.darkMode) {
      css += `[data-theme="dark"] {
`;
      colourModes.darkMode.forEach(({ token, value }) => {
        let resolved = value;
        if (typeof value === 'string' && value.startsWith('{')) {
          resolved = resolveReference(value, dictionary);
        }
        css += `  --${token.path.join('-')}: ${resolved};
`;
      });
      css += `}

`;
    }

    // ── TYPEFACE MODE LAYERS ──
    Object.entries(typefaceModes).forEach(([modeName, items]) => {
      if (modeName === 'sans') return; // skip default
      css += `[data-typeface="${modeName}"] {
`;
      items.forEach(({ token, value }) => {
        let resolved = value;
        if (typeof value === 'string' && value.startsWith('{')) {
          resolved = resolveReference(value, dictionary);
        }
        css += `  --font-family-base: ${resolved};
`;
      });
      css += `}

`;
    });

    // ── RESPONSIVE FONT SIZES ──
    // Extract fontSizeMode responsive breakpoints
    const fontSizeModes = allTokens.filter((t) => t.path[0] === 'fontSizeMode');
    const breakpoints = {
      mobile: '393px',
      tablet: '800px',
      desktop: '1280px',
      large: '1440px',
    };

    // Build responsive font size map from fontSizeMode tokens
    const responsiveSizes = {};
    fontSizeModes.forEach((t) => {
      const modeData = t.$extensions?.mode;
      if (!modeData) return;

      const tokenPath = t.path.slice(1).join('-'); // e.g., "heading-h1"
      if (!responsiveSizes[tokenPath]) responsiveSizes[tokenPath] = {};

      Object.entries(modeData).forEach(([bp, ref]) => {
        let resolved = ref;
        if (typeof ref === 'string' && ref.startsWith('{')) {
          resolved = resolveReference(ref, dictionary);
        }
        responsiveSizes[tokenPath][bp] = resolved;
      });
    });

    // Generate media queries
    Object.entries(breakpoints).forEach(([bp, width]) => {
      if (bp === 'mobile') return; // mobile is default (:root)
      css += `@media (min-width: ${width}) {
`;
      css += `  :root {
`;
      Object.entries(responsiveSizes).forEach(([tokenPath, sizes]) => {
        if (sizes[bp]) {
          css += `    --font-size-mode-${tokenPath}: ${sizes[bp]};
`;
        }
      });
      css += `  }
`;
      css += `}

`;
    });

    // ── UTILITY: Typography shorthand classes ──
    css += `/* Typography shorthand classes */
`;
    typographyComposites.forEach((t) => {
      const className = `.typography-${t.path.join('-')}`;
      const v = t.$value;
      css += `${className} {
`;
      css += `  font-family: var(--${t.path.join('-')}-fontFamily);
`;
      css += `  font-weight: var(--${t.path.join('-')}-fontWeight);
`;
      css += `  font-size: var(--${t.path.join('-')}-fontSize);
`;
      css += `  line-height: var(--${t.path.join('-')}-lineHeight);
`;
      css += `  letter-spacing: var(--${t.path.join('-')}-letterSpacing);
`;
      css += `}

`;
    });

    return css;
  },
});

/**
 * Tailwind: Theme config referencing CSS variables
 */
StyleDictionary.registerFormat({
  name: 'tailwind/standard-modes',
  formatter: ({ dictionary }) => {
    const allTokens = dictionary.allTokens;

    // Extract colors
    const colors = {};
    allTokens
      .filter((t) => t.$type === 'color' && (t.path[0] === 'hues' || t.path[0] === 'textColour' || t.path[0] === 'bgColour'))
      .forEach((t) => {
        const key = t.path.slice(1).join('-');
        colors[key] = `var(--${t.path.join('-')})`;
      });

    // Extract font families
    const fontFamily = {};
    allTokens
      .filter((t) => t.path.join('.') === 'fontFamilyMode')
      .forEach((t) => {
        const modeData = t.$extensions?.mode;
        if (modeData) {
          Object.entries(modeData).forEach(([modeName, ref]) => {
            let resolved = ref;
            if (typeof ref === 'string' && ref.startsWith('{')) {
              resolved = resolveReference(ref, dictionary);
            }
            fontFamily[modeName] = [resolved, 'sans-serif'];
          });
        }
      });

    // Extract font sizes (primitive + responsive)
    const fontSize = {};
    allTokens
      .filter((t) => t.$type === 'dimension' && t.scopes?.includes('FONT_SIZE'))
      .forEach((t) => {
        const key = t.path.slice(1).join('-'); // e.g., "size-fs+7"
        const val = getResolvedValue(t, dictionary);
        fontSize[key] = val;
      });

    // Typography composites for Tailwind plugin-style config
    const typography = {};
    allTokens
      .filter((t) => t.$type === 'typography')
      .forEach((t) => {
        const key = t.path.join('-');
        const v = t.$value;
        typography[key] = {
          css: {
            fontFamily: `var(--${key}-fontFamily)`,
            fontWeight: `var(--${key}-fontWeight)`,
            fontSize: `var(--${key}-fontSize)`,
            lineHeight: `var(--${key}-lineHeight)`,
            letterSpacing: `var(--${key}-letterSpacing)`,
          },
        };
      });

    // Spacing
    const spacing = {};
    allTokens
      .filter((t) => t.$type === 'dimension' && t.scopes?.includes('GAP'))
      .forEach((t) => {
        const key = t.path.slice(1).join('-');
        spacing[key] = getResolvedValue(t, dictionary);
      });

    // Border radius
    const borderRadius = {};
    allTokens
      .filter((t) => t.$type === 'dimension' && t.scopes?.includes('CORNER_RADIUS'))
      .forEach((t) => {
        const key = t.path.slice(1).join('-');
        borderRadius[key] = getResolvedValue(t, dictionary);
      });

    return `/** @type {import('tailwindcss').Config} */
module.exports = {
  darkMode: ['class', '[data-theme="dark"]'],
  content: ['./src/**/*.{html,js,jsx,ts,tsx,vue,svelte}'],
  theme: {
    extend: {
      colors: ${JSON.stringify(colors, null, 6)},
      fontFamily: ${JSON.stringify(fontFamily, null, 6)},
      fontSize: ${JSON.stringify(fontSize, null, 6)},
      spacing: ${JSON.stringify(spacing, null, 6)},
      borderRadius: ${JSON.stringify(borderRadius, null, 6)},
    },
  },
  plugins: [
    require('@tailwindcss/typography')({
      theme: ${JSON.stringify(typography, null, 6)},
    }),
  ],
};`;
  },
});

