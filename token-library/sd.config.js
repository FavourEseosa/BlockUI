/**
 * Extract mode data from tokens for a specific collection.
 * Returns: { modeName: [ { token, value }, ... ] }
 */
function extractModes(tokens, collectionName) {
  const modes = {};

  tokens.forEach((token) => {
    const figmaExt = token.$extensions?.figma;
    const tokenCollection = figmaExt?.collection?.name;
    const modeData = token.$extensions?.mode;

    if (tokenCollection !== collectionName || !modeData) return;

    Object.entries(modeData).forEach(([modeName, modeValue]) => {
      if (!modes[modeName]) modes[modeName] = [];
      modes[modeName].push({
        token,
        value: modeValue,
        varName: `--${token.path.join('-')}`,
      });
    });
  });

  return modes;
}

/**
 * Resolve a token reference like "{font.size.fs+7}" to its value.
 */
function resolveReference(ref, dictionary) {
  const cleanRef = ref.replace(/[{}]/g, '');
  const found = dictionary.allTokens.find(
    (t) => t.path.join('.') === cleanRef
  );
  return found ? found.$value : ref;
}

/**
 * Get the final resolved value for a token, handling references.
 */
function getResolvedValue(token, dictionary) {
  let value = token.$value;
  if (typeof value === 'string' && value.startsWith('{')) {
    value = resolveReference(value, dictionary);
  }
  return value;
}

