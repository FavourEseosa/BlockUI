StyleDictionary.registerTransform({
  name: 'size/pxToRem',
  type: 'value',
  transitive: true,
  matcher: (token) =>
    token.$type === 'dimension' &&
    (token.scopes?.includes('FONT_SIZE') ||
      token.path.some((p) => p.toLowerCase().includes('size'))),
  transformer: (token) => {
    const val = parseFloat(token.$value);
    if (isNaN(val)) return token.$value;
    return `${val / 16}rem`;
  },
});

StyleDictionary.registerTransform({
  name: 'font/familyQuote',
  type: 'value',
  transitive: true,
  matcher: (token) => token.$type === 'string' && token.scopes?.includes('FONT_FAMILY'),
  transformer: (token) => {
    const val = token.$value;
    // Don't quote if already has fallback or is a CSS var
    if (val.includes(',') || val.startsWith('var(')) return val;
    return `'${val}', sans-serif`;
  },
});

